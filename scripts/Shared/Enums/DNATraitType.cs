public enum DNATraitType
{
    // BioComponent related
    HungerRateMultiplier,       // Affects how quickly hunger increases
    ThirstRateMultiplier,       // Affects how quickly thirst increases

    // BrainComponent related
    MemoryDecayRateMultiplier,  // Affects how quickly memories decay

    // MuscleComponent related
    SpeedMultiplier,            // Affects base movement speed

    // GrowingComponent related
    MaturationRateMultiplier,   // Affects how quickly the Bloomy ages/progresses through life stages

    // SightComponent related
    SensoryRangeMultiplier,     // Affects the radius/extent of the sight area
}